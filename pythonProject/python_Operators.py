#Operator in python
#Arthmatic Operators
print("5+6", 5+6)
print("5-6", 5-6)
print("5*6", 5*6)
print("5**3", 5**3)
print("5/6", 5/6)
print("5//6", 5//6)
print("5%6", 5%6)

#Assignment Operator
x = 5
print(x)
x /= 7 # x = x/7
print(x)

#Bitwise Operator
#& , |
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11
print(0&1)

#Logical Operator
a = True
b = False
print(a and b)
print(a or b)

#Comparison Operator
i = 5
print(i < 6)
print(i <= 6)
print(i >= 6)
print(i == 6)
print(i != 6)

#Identity Opertor
print(5 == 5)
print(5 != 5)

#Membership Operator
list =[3,2,4,5,3,1,34]
print(5 in list)
print(5 not in list)